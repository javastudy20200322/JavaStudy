package summary.haesung.example.chapter02;

public class CharEx
{

	public static void main(String[] args)
	{
		char a = 'A';
		char b = 65;
		
		// a == b 는 true
		System.out.println(a == b);
		
		// 문자 A의 유니코드값인 65가 출력된다.
		System.out.println((int) a);
		System.out.println(b);
	}

}
